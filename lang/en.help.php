<div> 
<ul>
	<li><h5>How do I log in?</h5>
		<p>Log in using your last name, and your GWid. If you’re not sure what your GWid is, it can be retrieved at http://gwid.gwu.edu/ .</p>
	</li>
	<li><h5>How do I place a reservation?</h5>
		<ul>
			<li>Click on “Place a Reservation”.</li>
			<li>You’ll see a grid of times and rooms, starting with the current date.</li>
			<li>Look for the column for the time you’d like to reserve, and scan down to find an open room.</li>
			<li>When you find an open slot, click it to open a confirmation window.</li>
			<li>Click Save to place your reservation. </li>
		</ul>
	</li>
	<li><h5>What do the colors on the grid mean?</h5>
		<ul>
			<li>Green blocks (with horizontal stripes) indicate reservations that you have placed.</li>
			<li>Red blocks (with vertical stripes) are reservations that others have placed.</li>
			<li>Grey blocks are time slots that are in the past and are no longer available.</li>
		</ul>
	</li>
	<li><h5>Can I cancel a reservation?</h5>
	<p>Yes! If you are on the “Place a Reservation” page, simply click your existing reservation time on the grid. One the confirmation screen appears, check the “Delete?” box, and click Save.</p>
	<p>Alternatively, you can navigate to the Current Reservations page on the left menu, and click the Delete link next to the reservation that you want to get rid of.</p>
	</li>
	<li><h5>Who can reserve a room?</h5>
		<p>Any current GW students can place a reservation.</p>
		<p>Rooms are only for use by groups of two or more. If you are in a study room by yourself, you may be asked to leave.</p>
	</li>
	<li><h5>Are there any limits on how long I can reserve a room?</h5>
		<p>Each student can use two hours of reservation time per day. These two hours do not need to be consecutive.</p>
		<p>If you need more time than that, we suggest that you coordinate with your group, and have your members place back to back reservations.</p>
	<li><h5>How far in advance can I book a room?</h5>
		<p>Rooms can be reserved up to two weeks in advance.</p>
	<li><h5>Do I need to bring anything to claim my room?</h5>
		<p>You should receive an email confirmation of your reservation. We suggest that you print this out and bring it with you as proof of your reservation.<p>
	<li><h5>What rooms can be reserved through this system?</h5>
		<p>Any of the 36 group study rooms within Gelman Library can be reserved through this system. These rooms are located on the 3rd, 4th, 5th, and 6th floors.</p>
	<li><h5>Who should I contact with questions?</h5>
		<p>Please submit questions or comments through this <a href="http://www.gelman.gwu.edu/search-1/feedback/studyroomres-feedback-form">form</a>.<p>
	</li>
</ul>
